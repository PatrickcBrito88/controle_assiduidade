package domain.Modelo;


public class HierarquiaModel {
    private int idGerencia;
    private int idDepartamento;
    private int idDiretoria;
    private int idPresidencia;

    public int getIdGerencia() {
        return idGerencia;
    }

    public void setIdGerencia(int idGerencia) {
        this.idGerencia = idGerencia;
    }

    public int getIdDepartamento() {
        return idDepartamento;
    }

    public void setIdDepartamento(int idDepartamento) {
        this.idDepartamento = idDepartamento;
    }

    public int getIdDiretoria() {
        return idDiretoria;
    }

    public void setIdDiretoria(int idDiretoria) {
        this.idDiretoria = idDiretoria;
    }

    public int getIdPresidencia() {
        return idPresidencia;
    }

    public void setIdPresidencia(int idPresidencia) {
        this.idPresidencia = idPresidencia;
    }
}
