
package domain.Modelo;


public class NomeLocalModel {
    private String nomeGerencia;
    private String nomeDepartamento;
    private String nomeDiretoria;
    private String nomePresidencia;

    public String getNomeGerencia() {
        return nomeGerencia;
    }

    public void setNomeGerencia(String nomeGerencia) {
        this.nomeGerencia = nomeGerencia;
    }

    public String getNomeDepartamento() {
        return nomeDepartamento;
    }

    public void setNomeDepartamento(String nomeDepartamento) {
        this.nomeDepartamento = nomeDepartamento;
    }

    public String getNomeDiretoria() {
        return nomeDiretoria;
    }

    public void setNomeDiretoria(String nomeDiretoria) {
        this.nomeDiretoria = nomeDiretoria;
    }

    public String getNomePresidencia() {
        return nomePresidencia;
    }

    public void setNomePresidencia(String nomePresidencia) {
        this.nomePresidencia = nomePresidencia;
    }
    
    
}
